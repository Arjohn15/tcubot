export default function getSchoolMaps(
  imageWidth: number,
  imageHeight: number,
  floorLevel: string
) {
  let areas;
  switch (floorLevel) {
    case "1st": {
      areas = firstFloorAreas;
      break;
    }
    case "2nd": {
      areas = secondFloorAreas;
      break;
    }
    case "3rd": {
      areas = thirdFloorAreas;
      break;
    }
    case "4th": {
      areas = fourthFloorAreas;
      break;
    }
    default: {
      areas = firstFloorAreas;
    }
  }

  return areas.map(({ top, left, width, height, ...rest }) => ({
    ...rest,
    topPercent: (top / imageHeight) * 100,
    leftPercent: (left / imageWidth) * 100,
    widthPercent: (width / imageWidth) * 100,
    heightPercent: (height / imageHeight) * 100,
  }));
}
